    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        String acc_no=jTextField1.getText();
        String acc_type="";
        if(jRadioButton1.isSelected())
            acc_type="Saving Account";
        if(jRadioButton2.isSelected())
            acc_type="Current Account";
        
        String name=jTextField2.getText();
        String address=jTextArea1.getText();
        String mobile=jTextField3.getText();
        String email=jTextField4.getText();
        String adhar_no=jTextField5.getText();
        String pan_no=jTextField6.getText();
        String initial_amt=jTextField7.getText();
        
       if(acc_type.isEmpty())
          JOptionPane.showMessageDialog(null, "Please Select Correct Account Type");
       else if(name.isEmpty())
           JOptionPane.showMessageDialog(null, "Please Enter Customer Name");
       else if(address.isEmpty())
           JOptionPane.showMessageDialog(null, "Please Enter Address");
       else if(!isValidMobile(mobile))
           JOptionPane.showMessageDialog(null, "Please Enter Valid Mobile Number");
       else if(!isValidEmail(email))
           JOptionPane.showMessageDialog(null, "Please Enter Valid Email Id");
       else if(adhar_no.isEmpty())
           JOptionPane.showMessageDialog(null, "Please Enter Adharcard Number");
       else if(pan_no.isEmpty())
           JOptionPane.showMessageDialog(null, "Please Enter Pancard Number");
       else if(!isValidInitialAmount(initial_amt))
           JOptionPane.showMessageDialog(null, "Initial amount should be greater than or equal to 1000");
       else
       {
           NewAccountCreation nac=new NewAccountCreation();
           if(nac.doCreateAccount(acc_no, acc_type, name, address, mobile, email, adhar_no, pan_no, initial_amt))
                 JOptionPane.showMessageDialog(null, "Account Created Successfully");
           else
                 JOptionPane.showMessageDialog(null, "Error In Account Creation");
       }
    }//GEN-LAST:event_jButton1ActionPerformed

