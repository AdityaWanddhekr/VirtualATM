    public boolean isValidEmail(String email)
    {
        boolean flag=false;
        if(!email.isEmpty())
        {
            if(email.contains("@") && email.contains("."))
                flag=true;
        }
        return flag;
    }
    public boolean isValidMobile(String mobile)
    {
        boolean flag=false;
        if(!mobile.isEmpty())
        {
            if(mobile.length()==10)
            {
                try
                {
                    long num=Long.parseLong(mobile);
                    flag=true;
                }
                catch(Exception ex)
                {
                    System.out.println(ex);
                }
            }
        }
        return flag;
    }
    public boolean isValidInitialAmount(String initial_amt)
    {
       int initial=Integer.parseInt(initial_amt);
        boolean flag=false;
        if(!initial_amt.isEmpty())
        {
            if(initial>=1000)
                flag=true;
        }
        return flag;
    }
